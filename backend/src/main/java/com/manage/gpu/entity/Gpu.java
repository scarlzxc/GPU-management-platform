package com.manage.gpu.entity;

public class Gpu {
    private Long id;
    private String resource_owner;
    private String resource_user;
    private String host_name;
    private String ip;
    private String user;
    private String gpu_resource;
    private String username;
    private String password;
    private String ssd;
    private String disk;
    private String memory;
    private String cpu;
    private String os;
    private String remarks;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getResource_owner() {
        return resource_owner;
    }

    public void setResource_owner(String resource_owner) {
        this.resource_owner = resource_owner;
    }

    public String getResource_user() {
        return resource_user;
    }

    public void setResource_user(String resource_user) {
        this.resource_user = resource_user;
    }

    public String getHost_name() {
        return host_name;
    }

    public void setHost_name(String host_name) {
        this.host_name = host_name;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getGpu_resource() {
        return gpu_resource;
    }

    public void setGpu_resource(String gpu_resource) {
        this.gpu_resource = gpu_resource;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getSsd() {
        return ssd;
    }

    public void setSsd(String ssd) {
        this.ssd = ssd;
    }

    public String getDisk() {
        return disk;
    }

    public void setDisk(String disk) {
        this.disk = disk;
    }

    public String getMemory() {
        return memory;
    }

    public void setMemory(String memory) {
        this.memory = memory;
    }

    public String getCpu() {
        return cpu;
    }

    public void setCpu(String cpu) {
        this.cpu = cpu;
    }

    public String getOs() {
        return os;
    }

    public void setOs(String os) {
        this.os = os;
    }

    public String getRemarks() {
        return remarks;
    }

    public void setRemarks(String remarks) {
        this.remarks = remarks;
    }
}
